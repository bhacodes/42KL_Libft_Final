#include "libft.h"

static int	ft_wcount(char const *s, char c)
{
	int	i;
	int	word;

	i = 0;
	word = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i] != '\0')
			word++;
		while (s[i] && s[i] != c)
			i++;
	}
	return (word);
}

char	**ft_split(char const *s, char c)
{
	char	**split;
	size_t	i;
	size_t	j;
	size_t	k;

	i = 0;
	j = 0;
	k = 0;
	if (!s)
		return (NULL);
	split = (char **)malloc(sizeof(char *) * (ft_wcount(s, c) + 1));
	if (!split)
		return (NULL);
	while (s[i])
	{
		while (s[i] == c)
			i++;
		j = i;
		while (s[i] && s[i] != c)
			i++;
		if (i > j)
			split[k++] = ft_strndup(s + j, i - j);
	}
	split[k] = NULL;
	return (split);
}
