#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*fstr;
	unsigned int	i;

	i = 0;
	if (s)
	{
		fstr = malloc(sizeof(char) * (ft_strlen(s)) + 1);
		if (!fstr)
			return (NULL);
		while (s[i])
		{
			fstr[i] = f(i, s[i]);
			i++;
		}
		fstr[i] = '\0';
		return (fstr);
	}
	return (NULL);
}
